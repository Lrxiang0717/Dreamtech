package com.dreamtech.dt_web.daomain;

public class HomeSummary {
    //首页简述
    private String homeSummaryId;
    private String homeSummaryName;
    private String homeSummaryContent;

    public String getHomeSummaryId() {
        return homeSummaryId;
    }

    public void setHomeSummaryId(String homeSummaryId) {
        this.homeSummaryId = homeSummaryId;
    }

    public String getHomeSummaryName() {
        return homeSummaryName;
    }

    public void setHomeSummaryName(String homeSummaryName) {
        this.homeSummaryName = homeSummaryName;
    }

    public String getHomeSummaryContent() {
        return homeSummaryContent;
    }

    public void setHomeSummaryContent(String homeSummaryContent) {
        this.homeSummaryContent = homeSummaryContent;
    }
}
