package com.dreamtech.dt_web.daomain;

public class Finance {
    //财务管理
    private Integer financeId;
    private String financeMoney;
    private String financeProject;
    private String financeTransactor;
    private String financeMark;
    private String financeTime;

    public Integer getFinanceId() {
        return financeId;
    }

    public void setFinanceId(Integer financeId) {
        this.financeId = financeId;
    }

    public String getFinanceMoney() {
        return financeMoney;
    }

    public void setFinanceMoney(String financeMoney) {
        this.financeMoney = financeMoney;
    }

    public String getFinanceProject() {
        return financeProject;
    }

    public void setFinanceProject(String financeProject) {
        this.financeProject = financeProject;
    }

    public String getFinanceTransactor() {
        return financeTransactor;
    }

    public void setFinanceTransactor(String financeTransactor) {
        this.financeTransactor = financeTransactor;
    }

    public String getFinanceMark() {
        return financeMark;
    }

    public void setFinanceMark(String financeMark) {
        this.financeMark = financeMark;
    }

    public String getFinanceTime() {
        return financeTime;
    }

    public void setFinanceTime(String financeTime) {
        this.financeTime = financeTime;
    }
}
