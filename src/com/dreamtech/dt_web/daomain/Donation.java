package com.dreamtech.dt_web.daomain;

public class Donation {
    private Integer donationId;
    private String donationMoney;
    private String donationMember;
    private String donationMark;
    private String donationTime;

    public Integer getDonationId() {
        return donationId;
    }

    public void setDonationId(Integer donationId) {
        this.donationId = donationId;
    }

    public String getDonationMoney() {
        return donationMoney;
    }

    public void setDonationMoney(String donationMoney) {
        this.donationMoney = donationMoney;
    }

    public String getDonationMember() {
        return donationMember;
    }

    public void setDonationMember(String donationMember) {
        this.donationMember = donationMember;
    }

    public String getDonationMark() {
        return donationMark;
    }

    public void setDonationMark(String donationMark) {
        this.donationMark = donationMark;
    }

    public String getDonationTime() {
        return donationTime;
    }

    public void setDonationTime(String donationTime) {
        this.donationTime = donationTime;
    }
}
