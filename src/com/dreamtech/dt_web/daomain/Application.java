package com.dreamtech.dt_web.daomain;

public class Application {
    private Integer applicationId;
    private String applicationName;
    private String applicationSex;
    private String applicationClass;
    private String applicationPhone;
    private String applicationIntention;
    private String applicationIntroduce;

    public Integer getApplicationId() {
        return applicationId;
    }

    public void setApplicationId(Integer applicationId) {
        this.applicationId = applicationId;
    }

    public String getApplicationName() {
        return applicationName;
    }

    public void setApplicationName(String applicationName) {
        this.applicationName = applicationName;
    }

    public String getApplicationClass() {
        return applicationClass;
    }

    public void setApplicationClass(String applicationClass) {
        this.applicationClass = applicationClass;
    }

    public String getApplicationPhone() {
        return applicationPhone;
    }

    public void setApplicationPhone(String applicationPhone) {
        this.applicationPhone = applicationPhone;
    }

    public String getApplicationIntention() {
        return applicationIntention;
    }

    public void setApplicationIntention(String applicationIntention) {
        this.applicationIntention = applicationIntention;
    }

    public String getApplicationIntroduce() {
        return applicationIntroduce;
    }

    public void setApplicationIntroduce(String applicationIntroduce) {
        this.applicationIntroduce = applicationIntroduce;
    }

    public String getApplicationSex() {
        return applicationSex;
    }

    public void setApplicationSex(String applicationSex) {
        this.applicationSex = applicationSex;
    }
}
