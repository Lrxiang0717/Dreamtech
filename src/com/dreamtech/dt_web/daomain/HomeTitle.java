package com.dreamtech.dt_web.daomain;

public class HomeTitle {
    //首页标题和简介
    private String homeTitleId;
    private String homeTitleName;
    private String homeTitleContent;

    public String getHomeTitleId() {
        return homeTitleId;
    }

    public void setHomeTitleId(String homeTitleId) {
        this.homeTitleId = homeTitleId;
    }

    public String getHomeTitleName() {
        return homeTitleName;
    }

    public void setHomeTitleName(String homeTitleName) {
        this.homeTitleName = homeTitleName;
    }

    public String getHomeTitleContent() {
        return homeTitleContent;
    }

    public void setHomeTitleContent(String homeTitleContent) {
        this.homeTitleContent = homeTitleContent;
    }
}
